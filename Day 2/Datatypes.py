# subscripting
print("hello"[0])  # gives the first element of the string
print("hello"[-1])  # gives the last element of the string


# Integers
print(1 + 2 + 4)

# large numbers - similar to 123,456,789 for better understanding, the underscore will be removed by the computer before printing
print(123_456_789)

# floating point numbers
print(3.14)

# boolean
print(True)
print(False)


# type
print(type("hello"))
print(type(123))
print(type(1.5))
print(type(True))

# type conversions or type casting
print(int("123"))
print(float("1.2"))
print(bool("True"))
print(str(123))

